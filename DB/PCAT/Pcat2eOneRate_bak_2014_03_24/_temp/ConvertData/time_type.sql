             AND t13.LANGUAGE_CODE = t4.LANGUAGE_CODE
       LEFT OUTER JOIN PROVIDER_CLASS_KEY t14
          ON t1.PROVIDER_CLASS = t14.PROVIDER_CLASS
       LEFT OUTER JOIN PROVIDER_CLASS_REF t15
          ON     t14.PROVIDER_CLASS = t15.PROVIDER_CLASS
