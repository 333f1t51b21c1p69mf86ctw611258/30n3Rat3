          ON     t20.JURISDICTION = t21.JURISDICTION
             AND t21.SERVICE_VERSION_ID = 1
       LEFT OUTER JOIN JURISDICTIONS_VALUES t22
          ON     t20.JURISDICTION = t22.JURISDICTION
             AND t22.SERVICE_VERSION_ID = 1
             AND t22.LANGUAGE_CODE = t4.LANGUAGE_CODE
       LEFT OUTER JOIN POINT_CLASS_KEY t23
          ON t1.POINT_CLASS_ORIGIN = t23.POINT_CLASS
       LEFT OUTER JOIN POINT_CLASS_REF t24
          ON t23.POINT_CLASS = t24.POINT_CLASS AND t24.SERVICE_VERSION_ID = 1
       LEFT OUTER JOIN POINT_CLASS_VALUES t25
          ON     t23.POINT_CLASS = t25.POINT_CLASS
             AND t25.SERVICE_VERSION_ID = 1
             AND t25.LANGUAGE_CODE = t4.LANGUAGE_CODE
       LEFT OUTER JOIN POINT_CLASS_KEY t26
          ON t1.POINT_CLASS_TARGET = t26.POINT_CLASS
       LEFT OUTER JOIN POINT_CLASS_REF t27
          ON t26.POINT_CLASS = t27.POINT_CLASS AND t27.SERVICE_VERSION_ID = 1
       LEFT OUTER JOIN POINT_CLASS_VALUES t28
          ON     t26.POINT_CLASS = t28.POINT_CLASS
             AND t28.SERVICE_VERSION_ID = 1
             AND t28.LANGUAGE_CODE = t4.LANGUAGE_CODE
 WHERE t1.RESELLER_VERSION_ID = 2;